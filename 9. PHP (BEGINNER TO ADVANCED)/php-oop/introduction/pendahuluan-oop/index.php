<!-- 
  #### - kelebihan object oriented programming - ####
  - Representasi dunia nyata : ini menjadi kebutuhan dari program
  - Enkapsulasi dan abstrak data : data yang ada akan bisa diamankan dengan lebih baik
  - Reusability : penggunaan ulang dengan mudahnya bagiann bagian dari program yang kita buat
  - skalabilitas dan ekstensibilitas : ini akan memudahkan pengembang yang nanti pastinya programmnya akan menjadi lebih besar
  - kemudahan pengelolaan
  - Kolaborasi : akan lebih mudah dengan mengggunakan paradigma oop inni
  - digunakan oleh framework
-->

